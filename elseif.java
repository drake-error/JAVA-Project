public class elseif {
    public static void main(String args[])
    {
        int score = 70;

        if (score>35 && score<60)
        {
            System.out.print("Video Game");
        }
        else if(score>60 && score<90)
        {
            System.out.print("Iphone");
        }
        else if(score>90)
        {
            System.out.print("Macbook");
        }
        
      
    }
    

}