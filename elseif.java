public class elseif{
    public static void main(String args[])
    {
       int score = 75;

        if(score<50)
        { 
            System.out.println("Need to improve");
        }
        else if(score>=50 && score<=75)
        {
            System.out.println("Good job");
        }
        else
        {
            System.out.println("Very Good");
        }
        
}   

}